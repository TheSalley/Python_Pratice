# coding:utf-8

print('Addition: ', 1 + 2)  # Addition:  3
print('Subtraction: ', 2 - 1)  # Subtraction:  1
print('Multiplication: ', 2 * 3)  # Multiplication:  6
print('Division: ', 7 / 2)  # Division:  3.5
print('Division without the remainder: ', 7 // 6)  # Division without the remainder:  1
print('Modules: ', 3 % 2)  # Modules:  1
print('Exponential: ', 3 ** 2)  # Exponential:  9

print(3 > 2)  # True
print(1 < 1)  # False
print(2 >= 2)  # True
print(3 <= 2)  # False
print(2 == 3)  # False
print(1 != 2)  # True
print(2 < 3 < 5)  # True

print(3 > 2 and 5 > 7)  # False
print(1 > 2 or 4 < 5)  # True
print(not 3 > 2)  # False

print(1 is 1)  # True
print('a' is not 'a')  # False
print('a' in 'alan')  # True
print('a' not in 'jack')  # False
